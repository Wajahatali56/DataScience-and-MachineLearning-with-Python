
# Sort() - the numpy nd-array object has a function which is called sort() , and this will sort a specified array.

import numpy as np 
wajahat = np.array([3 , 8 , 2, 0 , 1])
print(np.sort(wajahat)) # This methode is like the copy()

# Sort array Alphabetically:
import numpy as np 
wajahat = np.array(["apple" ,"cherry" , "banana"])
print(np.sort(wajahat))

# Sort the boolean array:
import numpy as np 
wajahat = np.array([True , False , True])
print(np.sort(wajahat))

# Sorting the 2-D array:
import numpy as np 

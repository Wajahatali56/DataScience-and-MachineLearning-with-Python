
# Arrays are used to store multiple values in a single variables-
# Same as lists 
cars = ["frari" , "suzuki" , "od10"]
print(cars)

# Accessing the elements of an arrays
cars = ["frari" , "suzuki" , "od10"]
print(cars[1])

# Modifying the values of an arrays
cars = ["frari" , "suzuki" , "od10"]
cars[2] = "Honda"
print(cars)

# Length of an array
cars = ["frari" , "suzuki" , "od10"]
print(len(cars))

# Looping an array elements
cars = ["frari" , "suzuki" , "od10"]
for i in cars:
    print(i)

# Adding arrays elements
cars = ["frari" , "suzuki" , "od10"]
cars.append("4X4")    
print(cars)

# Removing an elements from array
cars = ['frari', 'suzuki', 'od10', '4X4']
cars.pop(3)
print(cars)

# Another way of removing elements
cars = ['frari', 'suzuki', 'od10', '4X4']
cars.remove("4X4")
print(cars)


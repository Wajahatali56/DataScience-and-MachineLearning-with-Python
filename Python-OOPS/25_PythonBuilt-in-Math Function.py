
# Python has a extensive maths built-in function.

x = min(10 , 15 , 20)
y = max(10 , 15 , 20)
print(x)
print(y)

# abs() - This function will return absolute positive value.

x = abs(-45.98)
print(x)

# pow(x , y) 

x = pow(2 , 4)
print(x)

# we can also import maths module externally
import math
x = math.ceil(2.2) # nearest to upward
y = math.floor(2.2) #nearest to downward
print(x)
print(y)

# math.pi
import math
x = math.pi
print(x)



